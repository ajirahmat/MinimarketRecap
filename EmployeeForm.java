    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String kodebarang = jTextField1.getText();
        String namabarang = jTextField2.getText();
        String harga = jTextField3.getText();
        String stock = jTextField4.getText();
        String keterangan = jTextField5.getText();
        RukoModel ruko = new RukoModel(kodebarang,namabarang,harga,stock,keterangan);
        try {
            db.CekData(kodebarang);
            if (db.rs.next()) {
                JOptionPane.showMessageDialog(rootPane, "maaf sudah ada barang dengan kode tersebut");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
            }
            else{
            db.InsertData(ruko);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            DataTable();
            }
        }
        catch(Exception e) {
        
        }

    }//GEN-LAST:event_jButton1ActionPerformed

